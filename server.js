// –ó–∞–≥—Ä—É–∑–∫–∞ –∫—ç—à–∞ –∏–∑ —Ñ–∞–π–ª–∞
async function loadCache() {
    try {
        const data = await fs.readFile(CACHE_FILE, 'utf8');
        const cacheArray = JSON.parse(data);

        const now = Date.now();
        let loaded = 0;
        let expired = 0;

        for (const [key, value] of cacheArray) {
            const cacheAge = now - new Date(value.timestamp).getTime();

            if (cacheAge < CACHE_TTL) {
                userCache.set(key, value);
                loaded++;
            } else {
                expired++;
            }
        }

        console.log(`üìÇ –ö—ç—à: ${loaded} –∞–∫—Ç—É–∞–ª—å–Ω—ã—Ö, ${expired} —É–¥–∞–ª–µ–Ω–æ (—Å—Ç–∞—Ä—à–µ 3 –Ω–µ–¥–µ–ª—å)`);

    } catch (error) {
        console.log('üìÇ –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π –∫—ç—à');
        userCache = new Map();
    }
}

