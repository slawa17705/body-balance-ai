function checkCache(req, res, next) {
    try {
        const userId = getUserId(req);
        const userData = req.body.userData;
        const path = req.path;
        const cacheKey = `${userId}_${path}`;

        console.log('üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫—ç—à–∞ –¥–ª—è:', userData?.name, '–≤–µ—Å:', userData?.weight, '–ø–æ–ª:', userData?.gender);

        const cached = userCache.get(cacheKey);

        if (cached && userData) {
            const cacheAge = Date.now() - new Date(cached.timestamp).getTime();
            const isExpired = cacheAge >= CACHE_TTL;

            // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö
            if (!isExpired && isUserDataMatch(userData, cached.userData)) {
                console.log(`‚úÖ –ö–≠–® –ü–û–î–û–®–ï–õ! –í–æ–∑—Ä–∞—Å—Ç: ${(cacheAge / (24 * 60 * 60 * 1000)).toFixed(1)} –¥–Ω–µ–π`);

                // –î–æ–±–∞–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏ –≤–µ—Å–∞
                const weightMessage = getWeightChangeMessage(
                    userData.weight,
                    cached.userData.weight,
                    userData.name || '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å'
                );

                const response = {
                    ...cached.response,
                    cached: true,
                    cacheAgeDays: (cacheAge / (24 * 60 * 60 * 1000)).toFixed(1)
                };

                if (weightMessage) {
                    response.weightChange = weightMessage;
                }

                return res.json(response);
            }

            console.log(`‚ùå –ö—ç—à –Ω–µ –ø–æ–¥–æ—à–µ–ª:`, {
                expired: isExpired,
                dataMatch: isUserDataMatch(userData, cached.userData),
                ageDays: (cacheAge / (24 * 60 * 60 * 1000)).toFixed(1)
            });
        }

        console.log(`üîÑ –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –Ω–æ–≤—ã–µ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏`);

        req._cacheKey = cacheKey;
        req._userData = userData;

        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –≤–µ—Å –¥–ª—è —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
        if (cached) {
            req._previousWeight = cached.userData.weight;
        }

        const originalJson = res.json;
        res.json = function (data) {
            if (data.success && data.advice && req._userData) {
                // –î–æ–±–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏ –≤–µ—Å–∞ –µ—Å–ª–∏ –µ—Å—Ç—å
                if (req._previousWeight && req._userData.weight) {
                    const weightMessage = getWeightChangeMessage(
                        req._userData.weight,
                        req._previousWeight,
                        req._userData.name || '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å'
                    );

                    if (weightMessage) {
                        data.weightChange = weightMessage;
                    }
                }

                userCache.set(req._cacheKey, {
                    response: data,
                    userData: req._userData,
                    timestamp: new Date().toISOString()
                });

                saveCacheAsync();

                data.cached = false;
                data.generatedAt = new Date().toISOString();
            }
            return originalJson.call(this, data);
        };

        next();

    } catch (error) {
        console.error('–û—à–∏–±–∫–∞ checkCache:', error);
        next();
    }
}

