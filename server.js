const userCache = new Map();

function getUserId(req) {
    const userData = req.body.userData || {};
    const userString = JSON.stringify({
        name: userData.name,
        age: userData.age,
        weight: userData.weight,
        height: userData.height,
        goal: userData.goal,
        activity: userData.activity
    });
    const hash = require('crypto').createHash('md5').update(userString).digest('hex').slice(0, 12);
    return `user_${hash}`;
}

function checkCache(req, res, next) {
    const userId = getUserId(req);
    const path = req.path;
    const cacheKey = `${userId}_${path}`;
    const cached = userCache.get(cacheKey);

    if (cached) {
        console.log(`📦 Кэш для ${path}`);
        return res.json(cached.response);
    }

    req._cacheKey = cacheKey;
    const originalJson = res.json;
    res.json = function(data) {
        if (data.success && data.advice) {
            userCache.set(req._cacheKey, { response: data, timestamp: new Date() });
            data.cached = false;
        }
        return originalJson.call(this, data);
    };
    next();
}

function calculateBMIAndObesity(weight, height) {
    if (!weight || !height) return { bmi: '0.0', category: 'Не определено', obesityDegree: 0 };
    const heightM = height / 100;
    const bmi = weight / (heightM * heightM);
    let category = '';
    let obesityDegree = 0;
    if (bmi < 18.5) category = 'Недостаточная масса';
    else if (bmi < 25) category = 'Нормальная масса';
    else if (bmi < 30) { category = 'Избыточная масса'; obesityDegree = 0; }
    else if (bmi < 35) { category = 'Ожирение I степени'; obesityDegree = 1; }
    else if (bmi < 40) { category = 'Ожирение II степени'; obesityDegree = 2; }
    else { category = 'Ожирение III степени'; obesityDegree = 3; }
    return { bmi: bmi.toFixed(1), category, obesityDegree };
}

