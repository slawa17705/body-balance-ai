            emoji: 'üìà',
            title: '–ò–∑–º–µ–Ω–µ–Ω–∏–µ –≤–µ—Å–∞',
            message: `${userName}, –≤–∞—à –≤–µ—Å —É–≤–µ–ª–∏—á–∏–ª—Å—è –Ω–∞ ${absDiff.toFixed(1)} –∫–≥`,
